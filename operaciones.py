# operaciones Matematicos

name = "Vals" # = es operador de asignacion 

print(10 + 10) # operador suma +
print(10 - 5) # operador resta -
print(20 * 5) #operador multiplicacion *
print(20 / 5) #operador divicion flotante /
print(20 // 5)#operador division entera //
print(2 ** 5) #operador potencia **
print(10 % 3) #operador modulo %

# operaciones de comparacion
print(10 < 10) # menor que 
print(10 > 10) #mayor que 
print(10 >= 10) #mayor o igual
print(10 <= 10) # menor o igual 
print(10 == 10) # igual
print(10 != 10) # diferente

 #operaciones logicos
print(True and True) #AND
print(True or True) #OR
print(not True) #NOT