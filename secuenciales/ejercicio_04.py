# dados dos numeros muestra la suma,
# resta, multiplicacion y divicion 

num_1 = int(input("ingresa un numero: "))
num_2 = int(input("ingresa otro numero: "))

suma = num_1 + num_2
resta = num_1 - num_2
multi = num_1 * num_2
divi = num_1 / num_2

print('suma:', suma)
print('resta:', resta)
print('multiplicacion:', multi)
print('divicion:', divi)
